CREATE DATABASE library_db;
USE library_db;

CREATE TABLE BOOK (
    bookId INT AUTO_INCREMENT PRIMARY KEY,
    bookName VARCHAR(200) NOT NULL,
    quantity INT CHECK (quantity >= 0),
    price DECIMAL(10,2) DEFAULT 5000
);


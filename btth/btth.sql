CREATE TABLE BORROW_BOOKS (
    borrowId INT AUTO_INCREMENT PRIMARY KEY,
    bookId INT,
    borrowDate DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (bookId) REFERENCES BOOK(bookId)
);
